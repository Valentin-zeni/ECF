var livreA = document.getElementById("fr").addEventListener('click', vernes)
function vernes() {
  var livre1 = document.getElementById('livre1');
  if (livre1.style.display === 'none') {
    livre1.style.display = 'block';
  } else {
    livre1.style.display = 'none';
  }
}
